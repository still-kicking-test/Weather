import Foundation
import UIKit

public extension UIFont {
    static let defaultFont: UIFont = .systemFont(ofSize: 15)
    static let defaultFontBold: UIFont = .systemFont(ofSize: 15, weight: .bold)
    static let largeFont: UIFont = .systemFont(ofSize: 18)
    static let largeFontBold: UIFont = .systemFont(ofSize: 18, weight: .bold)
    static let veryLargeFont: UIFont = .systemFont(ofSize: 24)
    static let veryLargeFontBold: UIFont = .systemFont(ofSize: 24, weight: .bold)
}
